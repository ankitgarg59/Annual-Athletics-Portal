          <h2>Long Jump</h2><br>
          <p>The long jump (historically called the broad jump) is a track and field event in which athletes combine speed, strength and agility in an attempt to leap as far as possible from a take off point. </p><br>
          
        </div>

        <div class="col-sm-4">
          <h2>High Jump</h2><br>
          <p>The high jump is a track and field event in which competitors must jump unaided over a horizontal bar placed at measured heights without dislodging it.In its modern most practised format, a bar is placed between two standards with a crash mat for landing. In the modern era, athletes run towards the bar and use the Fosbury Flop method of jumping, leaping head first with their back to the bar. </p><br>
         
       </div>

        <div class="col-sm-4">
          <h2>Triple Jump</h2><br>
          <p>The triple jump, sometimes referred to as the hop, step and jump or the hop, skip and jump, is a track and field event, similar to the long jump.As a group, the two events are referred to as the "horizontal jumps." The competitor runs down the track and performs a hop, a bound and then a jump into the sand pit. The triple jump was inspired by the ancient Olympic Games and has been a modern Olympics event since the Games' inception in 1896.</p><br>
          
