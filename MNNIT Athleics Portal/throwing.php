<h1 align="center">Throwing Events</h1><hr>

  <p><h4><ul>Throwing sports, or throwing games, are physical, human competitions where the outcome is measured by a player's ability to throw an object.The two primary forms are throwing for distance and throwing at a given target or range. 
  The four most prominent throwing for distance sports are in track and field are :</ul></h4></p><br><hr>    
      
</div>
      
<div class="row">
       <div class="col-sm-3">
          <h2>Shotput Throw</h2><br>
          <p>The shot put is a track and field event involving "throwing" & "putting" (throwing in a pushing motion) a heavy spherical object—the shot—as far as possible. </p><br>
          
        </div>

        <div class="col-sm-3">
          <h2>Discus Throw</h2><br>
          <p>The discus throw (About this sound pronunciation) is a track and field event in which an athlete throws a heavy disc—called a discus—in an attempt to mark a farther distance than their competitors.</p><br>
         
       </div>

       <div class="col-sm-3">
          <h2>Hammer Throw</h2><br>
          <p>The hammer throw is one of the four throwing events in regular track and field competitions, along with the discus throw, shot put and javelin. The "hammer" used in this sport is not like any of the tools also called by that name. It consists of a metal ball attached by a steel wire to a grip. The size of the ball varies between men's and women's competitions. </p><br>
          
        </div>

        <div class="col-sm-3">
          <h2>Javelin Throw</h2><br>
          <p>The javelin throw is a track and field event where the javelin, a spear about 2.5 m (8 ft 2 in) in length, is thrown. 
			The javelin thrower gains momentum by running within a predetermined area. Javelin throwing is an event of both the men's decathlon and the women's heptathlon.</p><br>
         
       </div>
