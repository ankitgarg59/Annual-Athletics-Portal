<h1 align="center">Racing Events</h1><hr>

  <p><h4><ul>In sport, racing is a competition of speed, against an objective criterion, usually a clock or to a specific point. The competitors in a race try to complete a given task in the shortest amount of time. Typically this involves traversing some distance, but it can be any other task involving speed to reach a specific goal.A race may be run continuously to finish or may be made of several segments called heats, stages or legs. A heat is usually run over the same course at different times. A stage is a shorter section of a much longer course or a time trial.</ul></h4></p><br><hr>    
      
