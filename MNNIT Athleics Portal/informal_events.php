          <h3>Slow Cycling Race</h3><br>
          <p>This is conducted for both boys and girls separately.A participant, who will reach to destination in maximum time without putting down his or her foot on ground and without stoping, will be the winner. </p><br>
          
        </div>

        <div class="col-sm-3">
          <h3>Sack Race</h3><br>
          <p>This is also conducted for both boys and girls separately.The sack race or gunny sack race is a competitive game in which participants place both of their legs inside a sack or pillow case that reaches their waist or neck and hop forward from a starting point toward a finish line. The first person to cross the finish line is the winner of the race.</p><br>
         
       </div>

       <div class="col-sm-3">
          <h3>Brick Race</h3><br>
          <p>This event includes one boy and one girl as one participant.Boy will place one brick at a distance, where his partner can comfortably place his one leg and then both the leg.Now the boy will take the other brick (lying on ground as his partner has moved forward) and again place at a distance so that his partner can move forward. The couple who reaches the finish line first is the winner.</p><br>
          
        </div>

        <div class="col-sm-3">
          <h3>Three Leg Race</h3><br>
          <p>This event includes one boy and one girl as one participant.Boy and girl tie their one leg and their other legs are free and they need to cover some distane.The couple who reaches the finish line first is the winner.
